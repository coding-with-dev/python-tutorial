str = "Learn Python Programming with Practical"

print(str[:]) #Return full string bcz no range is given

print(str[:5]) #Return string from starting till index 5

print(str[5:]) #Return string from index 5 till the end

print(str[-9:]) #Use negative index as starting and till end

print(str[::]) #Print full string as no range is given and no step is given

print(str[::2]) #return string but every 2nd character will be print

print(str[:10:2]) #range from start till index 10 and every 2nd to be printed

print(str[6::3]) #range start from index 6 and go till end and every 3rd char to be printed
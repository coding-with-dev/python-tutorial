arr_list = ["Spiderman", "Ironman", "Superman", "Batman", "Antman", "Black Panther", "Captain America"]

#Print all list items
print(arr_list)

#Print all item starting from 2nd index
print(arr_list[2:])

#print all item from starting till 5th index
print(arr_list[:5])

#print item from 2nd index to 5th index
print(arr_list[2:5])

#print item from negative index
print(arr_list[-3:-1])

#print item from list but every 2nd element
print(arr_list[::2])

#print item from list but reverse order
print(arr_list[2::-1])